#[cfg(test)]
mod tests {
    use super::*;

    #[test]
    fn test_status_constants_exported() {
        // Verify constants are accessible
        assert_eq!(PSTATUS_READY, 0);
        assert_eq!(PSTATUS_DOWNLOADING, 1);
        assert_eq!(PSTATUS_UPLOADING, 2);
    }

    #[test]
    fn test_sync_type_constants() {
        assert_eq!(PSYNC_DOWNLOAD_ONLY, 1);
        assert_eq!(PSYNC_UPLOAD_ONLY, 2);
        assert_eq!(PSYNC_FULL, 3);
    }

    #[test]
    fn test_status_helper_functions() {
        assert_eq!(status_to_string(PSTATUS_READY), "Ready");
        assert!(is_syncing(PSTATUS_DOWNLOADING));
        assert!(!is_syncing(PSTATUS_READY));
        assert!(is_error_status(PSTATUS_LOGIN_REQUIRED));
        assert!(needs_user_action(PSTATUS_BAD_LOGIN_DATA));
    }
}
