//! FFI bindings to the pclsync C library.
//!
//! This module provides:
//! - Raw unsafe FFI function declarations
//! - C type definitions and conversions
//! - Callback trampolines for C-to-Rust callbacks
//!
//! # Safety
//!
//! All functions in this module that call into C code are unsafe.
//! Users should prefer the safe wrappers in the `wrapper` module.
//!
//! # Submodules
//!
//! - `raw`: Raw FFI function declarations for pclsync C functions
//! - `types`: C struct type definitions (bindgen-generated and manual)
//! - `callbacks`: Callback trampolines and safe registration functions
//!
//! # Usage
//!
//! The FFI layer is split into three main parts:
//!
//! 1. **Types** (`types` module): Contains struct definitions generated by bindgen
//!    and Rust constants that mirror the C library's constants. Use these types
//!    when you need to pass data to or receive data from C functions.
//!
//! 2. **Raw Functions** (`raw` module): Contains the actual FFI function declarations.
//!    All functions are unsafe and require careful attention to pointer validity,
//!    memory ownership, and thread safety.
//!
//! 3. **Callbacks** (`callbacks` module): Contains safe callback registration and
//!    trampoline functions for C-to-Rust callback invocation.
//!
//! # Example
//!
//! ```ignore
//! use console_client::ffi::{raw, types, callbacks};
//! use console_client::utils::cstring::to_cstring;
//!
//! // Register callbacks before starting sync
//! callbacks::register_status_callback(|status| {
//!     println!("Status changed: {}", status.status);
//! });
//!
//! // Initialize the library
//! unsafe {
//!     let result = raw::psync_init();
//!     if result != 0 {
//!         panic!("Failed to initialize pclsync");
//!     }
//! }
//!
//! // Start sync with the trampoline callback
//! unsafe {
//!     raw::psync_start_sync(
//!         Some(callbacks::status_callback_trampoline),
//!         None,
//!     );
//! }
//!
//! // Get current status
//! let mut status = types::pstatus_t::default();
//! unsafe {
//!     raw::psync_get_status(&mut status);
//! }
//!
//! println!("Current status: {}", types::status_to_string(status.status));
//! ```
//!
//! # Thread Safety
//!
//! The pclsync library uses internal threading. Key thread safety notes:
//!
//! - Callbacks (status, event, notification) are called from a dedicated callback thread
//! - Callbacks are guaranteed not to overlap with each other
//! - Most API functions should be called from the main application thread
//! - Some functions like `psync_network_exception()` are safe to call from any thread
//!
//! # Memory Management
//!
//! Memory returned by pclsync functions typically needs to be freed:
//!
//! - Use `psync_free()` for memory allocated by the library
//! - Some functions return pointers that should NOT be freed (documented per-function)
//! - Always check for null pointers before dereferencing
//!
//! # Callbacks
//!
//! The `callbacks` module provides:
//! - Safe Rust callback registration via `register_*_callback()` functions
//! - Trampoline functions (`*_callback_trampoline`) for C-to-Rust callback invocation
//! - Thread-safe callback data management with panic guards
//! - `CallbackConfig` builder for configuring multiple callbacks at once
//! - Application-level overlay callbacks for interactive mode

